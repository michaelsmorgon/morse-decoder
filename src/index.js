const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let wordLength = 10;
    let res = expr.split('**********').map(val => {
        let offset = 0;
        let morse = [];
        while (offset < val.length) {
            let letter = val.slice(offset, offset + wordLength);
            morse.push(MORSE_TABLE[letter.replace(/00/g, '').replace(/10/g, '.').replace(/11/g, '-')]);
            offset += wordLength;
        }
        
        return morse.join('');
    });
    
    return res.join(' ');
}

module.exports = {
    decode
}